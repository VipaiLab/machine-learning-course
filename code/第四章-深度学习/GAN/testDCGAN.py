# Discriminator Code

class Discriminator(nn.Module):
    def __init__(self, ngpu):
        super(Discriminator, self).__init__()
        self.ngpu = ngpu
        self.main = nn.Sequential(
            # input is (nc) x 64 x 64
            nn.Conv2d(nc, ndf, 4, 2, 1, bias=False),
            nn.LeakyReLU(0.2, inplace=True),
            # state size. (ndf) x 32 x 32
            nn.Conv2d(ndf, ndf * 2, 4, 2, 1, bias=False),
            nn.BatchNorm2d(ndf * 2),
            nn.LeakyReLU(0.2, inplace=True),
            # state size. (ndf*2) x 16 x 16
            nn.Conv2d(ndf * 2, ndf * 4, 4, 2, 1, bias=False),
            nn.BatchNorm2d(ndf * 4),
            nn.LeakyReLU(0.2, inplace=True),
            # state size. (ndf*4) x 8 x 8
            nn.Conv2d(ndf * 4, ndf * 8, 4, 2, 1, bias=False),
            nn.BatchNorm2d(ndf * 8),
            nn.LeakyReLU(0.2, inplace=True),
            # state size. (ndf*8) x 4 x 4
            nn.Conv2d(ndf * 8, 1, 4, 1, 0, bias=False),
            nn.Sigmoid()
        )

    def forward(self, input):
        return self.main(input)


num_batches = 100

batch_size = 100

# For each batch, generate one image which has the maximum score from the discriminator.
gen_num_per_batch = 1

nz = 100

# The name of the generated image.
gen_image = 'result.png'

device = torch.device("cuda:0" if torch.cuda.is_available() else "cpu")

#load the generator and the discriminator.
netD = torch.load('./models/netD.pkl', map_location='cpu')
netG = torch.load('./models/netG.pkl', map_location='cpu')

result = []
for _ in range(num_batches):
    noise = torch.randn(batch_size, nz, 1, 1, device=device)
    fake_img = netG(noise)
    scores = netD(fake_img).data

    #select the images which have the maximum scores from the discriminator. 
    sorted, indices = torch.sort(scores,0, descending=True)
    newIndices = indices.numpy()
    for ii in range(gen_num_per_batch):
        result.append(fake_img.data[newIndices[ii,0,0,0]])

# Save images;
vutils.save_image(torch.stack(result),gen_image,normalize = True, range=(-1,1))
